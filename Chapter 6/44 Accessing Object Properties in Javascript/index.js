// Accessing Object Properties in Javascript

var obj = {
  x: 10,
  y: 20,
  z: 30,
};

// 1st wayaz dot Notation
console.log(obj);
console.log(obj.x);
console.log(obj.y);
console.log(obj.z);

// 2nd wayaz Array Notation
console.log(obj["x"]);
console.log(obj["y"]);
console.log(obj["z"]);

var show = "x";
console.log(obj[show]);
