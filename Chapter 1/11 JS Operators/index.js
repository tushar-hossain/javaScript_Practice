// JS Operators

// 1. Arithmetic operator => +, -, *, /, %, ++, --
var a = 10;
var b = 3;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a++);
console.log(a);
console.log(++a);
console.log(a--);
console.log(a);
console.log(--a);

// 2. Assignment operator => =, +=, -=, *=, /=, %=

console.log((a += b));
console.log((a -= b));
console.log((a *= b));
console.log((a /= b));

// 3. Comparison operator => ==, !=, >, <, >=, <=, ===, !==
console.log(a != b);

// 4. Bitwase operator => &, |, ~, ^, <<, >>

// 5. Logical operator => AND &&, OR ||, NOT !
console.log(a && b);

// 6. typeof operator
console.log(typeof a);
