// JS For Loop

for (var i = 0; i < 10; i++) {
  console.log("Tushar Hossain");
}

//  1 to 100 number print for loop
for (var i = 1; i <= 100; i++) {
  console.log(i);
}

for (var i = 0; i < 100; i++) {
  console.log(i + 1);
}

// Odd number
for (var i = 1; i < 100; i += 2) {
  console.log(i);
}

// // Even number
for (var i = 2; i < 100; i += 2) {
  console.log(i);
}
